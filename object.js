function NamaKotaDanProvinsi() {
  this.kota = "";
  this.provinsi = "";
}

const result1 = new NamaKotaDanProvinsi();
result1.kota = "Kota Kupang";
result1.provinsi = "Nusa Tenggara Timur";
console.log(result1);

